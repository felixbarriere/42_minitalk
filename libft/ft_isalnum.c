#include <stdio.h>
#include <ctype.h>

int	ft_isalnum(int c)
{
	if ((c >= 97 && c <= 122) || (c >= 65 && c <= 90))
	{
		return (1);
	}
	else if (c >= 48 && c <= 57)
	{
		return (1);
	}
	return (0);
}
